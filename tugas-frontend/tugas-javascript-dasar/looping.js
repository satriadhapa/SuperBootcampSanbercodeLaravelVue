// Soal Looping
// Jawaban Looping While

console.log("LOOPING PERTAMA");
var i = 2;
while (i <= 20) {
    console.log(i + " - I love coding so much");
    i += 2;
}

console.log("LOOPING KEDUA");
var j = 20;
while (j >= 2) {
    console.log(j + " - I will become a laravel developer and data scientist");
    j -= 2;
}
